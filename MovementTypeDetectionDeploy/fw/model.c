/*
* Try read data from model.
* 
*  @param data_out Output features. Output float[3].
*  @return IPWIN_RET_SUCCESS (0) or IPWIN_RET_NODATA (-1), IPWIN_RET_ERROR (-2), IPWIN_RET_STREAMEND (-3)
*/
int IMAI_dequeue(float *restrict data_out) {    
    __HOOK_REGION(true, 0);
    __RETURN_ERROR(fixwin_dequeue(_K2, _K1, 100, 10));
    __HOOK_REGION(false, 0);
    __HOOK_REGION(true, 1);
    mtb_model_f32_to_quantized_int8(_K7, _K1, 600, _K6, 3, _K5);
    __HOOK_REGION(false, 1);
    dequantize_d8_to_f32(_K6, data_out, 3, 0.00390625, -128);
    return 0;
}

